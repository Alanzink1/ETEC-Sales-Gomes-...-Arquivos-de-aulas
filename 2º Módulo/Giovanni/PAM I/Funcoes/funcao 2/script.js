// Função com parâmetro e com retorno
// Nome da função: calcularAreaTriangulo
// Parâmetros: base e altura
// Retorno: área do triângulo

function calcularAreaTriangulo(base, altura) {
    // let serve para definir uma variável de escopo local
    // somente existirá no escopo em que ela foi criada
    let area = (base * altura) / 2;
    return area;
}